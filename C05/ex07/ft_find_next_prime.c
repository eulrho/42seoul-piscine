int	ft_find_next_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (2);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			break ;
		i++;
	}
	if (i <= nb / i)
		return (ft_find_next_prime(nb + 1));
	else
		return (nb);
}
