char	*ft_strupcase(char *str)
{
	char	*ret;

	ret = str;
	while (*ret)
	{
		if (*ret >= 'a' && *ret <= 'z')
			*ret -= ('a' - 'A');
		ret++;
	}
	return (str);
}
